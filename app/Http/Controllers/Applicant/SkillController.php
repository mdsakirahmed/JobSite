     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'skill' => 'required',
        ]);

        //Database
        $skill = new Skill();

        $skill->applicant_id = Auth::user()->id;
        $skill->name         = $request->input('skill');
        $skill->save();

        return $skill;
    }

